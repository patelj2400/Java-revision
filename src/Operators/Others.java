package Operators;

public class Others {
    public static void main(String[] args) {

        // Other Operator
        // Increment and Decrement Operator
        // ++ and --

        System.out.println("Increment Operators");
        int x = 5;
        int y = 6;


        x++; // x = x +1;
        System.out.println(x);

        y--; // y = y+1;
        System.out.println(y);

    }
}
